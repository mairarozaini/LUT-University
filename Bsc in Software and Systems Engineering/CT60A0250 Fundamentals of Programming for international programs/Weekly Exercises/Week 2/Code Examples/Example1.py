print("Sam says, \"Hello!\"")
print() # Empty line

# Here we have single quote inside single quotest
print('Timmy\'s balloon is red.')

# String contains both ' and "
print("John says, \"The balloon\'s color is red.\"")
print()

# Newline character '\n' starts a new line
print("This string\nspans multiple\nlines.")
print()

# You can print also '\'
print("Here is a backslash: \\")

# Tabular is used for alignment
print("1.\tShark\n2.\tShrimp\n10.\tSquid")
print()

# NOTE THAT THERE CAN BE EMPTY LINES
# PYTHON DOES NOT CARE ABOUT THEM



