# > Greater than: True if the left operand is greater than the right
print(r"5 > 3", 5 > 3, sep = "\t\t")
print(r"2 > 4", 2 > 4, sep = "\t\t")

# < Less than: True if the left operand is less than the right
print(r"-1.1 < 6", -1.1 < 6, sep = "\t")
print(r"0 > 0", 0 > 0, sep = "\t\t")

# == Equal to: True if both operands are equal	
print(r"-3 == -3", -3 == -3, sep = "\t")
print(r"5 == -5", 5 == -5, sep = "\t\t")


# != Not equal to â€“ True if operands are not equal	
print(r"-3 != -3", -3 != -3, sep = "\t")
print(r"1.0 != 0.999", 1.0 != 0.999, sep = "\t")

# >= Greater than or equal to: True if left operand is greater than or equal to the right	x >= y
print(r"2.8 >= 4", 2 >= 4, sep = "\t")
print(r"3 >= 3", 3 >= 3, sep = "\t\t")

# <= Less than or equal to: True if left operand is less than or equal to the right	x <= y
print(r"2 <= 4", 2 <= 4, sep = "\t\t")
print(r"-1 <= -1", -1 <= -1, sep = "\t")
